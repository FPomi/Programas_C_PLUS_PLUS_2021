#include "Columna.h"

using namespace std;

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

Columna::Columna() {
	celdas = new Lista<string>;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

Columna::~Columna() {
	delete celdas;
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

void Columna::agregarCelda(string dato) {
	celdas->agregar(dato);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

void Columna::agregarCelda(string dato, int posicion) {
	celdas->agregar(dato, posicion);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

void Columna::setCelda(string dato, int posicion) {
	celdas->asignar(dato, posicion);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

string Columna::getCelda(int posicion) {
	return celdas->obtener(posicion);
}

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------//

int Columna::getTamanio() {
	return celdas->contarElementos();
}

